
1.功能:
	表切换(BCP辅助)
	
2.主要流程:
	遍历切换配置表(etl.BcpSTableCfg),根据文件目录查找是否有新的文件,若有,将待导入文件插入导入队列,然后记录初始化日志.
		导入队列处理作业检查到需要执行导入的行后,根据该行配置执行导入动作,导入成功后删除文件.
	
3.主要表:
	etl.BcpSTableCfg	切换配置
	
4.存储过程:
	4.1) etl.Job_Etl_SwitchBcpTable	查找需要执行切换的行,调用(4.2)执行切换
	4.2) etl.Etl_SwitchBcpTable		执行切换
	
5.作业:
	Etl_SwitchBcpTable		切换作业
	
6.配置说明:
	6.1) etl.EtlCfg
	
	EtlName						配置名(后续配置表需要与其一致)
	Folder						数据文件目录
	PeriodType					时期类型{1:年,2:半年,3:季度,4:月,5:周,6:日,7:时,8:分}
	FileName					文件名(前缀)(格式:FileName[时期][SrvID].txt),时期格式{1:yyyy,2:yyyy01/07,3:yyyy01/04/07/09,4:yyyyMM,5:yyyyww,6:yyyyMMdd,7:yyyyMMdd_hh,8:yyyyMMdd_hhmm}
	DBName, SchemaName, TName	目标表
	t							列分割串
	r							行分割串
